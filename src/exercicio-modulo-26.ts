function multiply(a: number, b: number): number {
  return a * b;
}

function greet(name: string): string {
  return `Olá ${name}`;
}

// Exemplos de utilização da função multiply
console.log(multiply(2, 3));     // Saída: 6
console.log(multiply(5, 4));     // Saída: 20
console.log(multiply(-2, 6));    // Saída: -12

// Exemplos de utilização da função greet
console.log(greet("Aline"));     // Saída: Olá Aline
console.log(greet("Rafael"));       // Saída: Olá Rafael
console.log(greet("Carolina"));     // Saída: Olá Carolina
